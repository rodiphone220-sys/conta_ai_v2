// ============ SELLAR ============
async function sellarYTimbrar() {
  console.log('='.repeat(60));
  console.log('🚀 CFDI 4.0 - Sellado y Timbrado');
  console.log('='.repeat(60));

  // 1. Generar XML
  const { xml, noCertificado } = generarXML();
  console.log('✅ XML generado');
  
  // Guardar XML sin sellar
  fs.writeFileSync('cfdi-sin-sellar.xml', xml, 'utf-8');
  console.log('💾 Guardado: cfdi-sin-sellar.xml');

  // 2. Generar cadena original
  const cadenaOriginal = generarCadenaOriginal(xml);
  console.log('\n🔗 Cadena original:');
  console.log(cadenaOriginal);
  console.log('\n📋 Cadena esperada:');
  console.log('||4.0|F|0000000001|2026-04-01T12:30:00|03|00001000000518570370|3500|MXN|4060|I|01|PUE|87500|GUIM980220L20|MARIO ALBERTO GUERRERO IBARRA|626|RGO810620KC3|RODOLFO GARCIA ORTIZ|G03|84111500|1|E48|SERVICIO|Servicio de ambientación musical|3500.00|3500.00|02|3500.00|002|Tasa|0.160000|560.00|3500.00|002|Tasa|0.160000|560||');
  
  fs.writeFileSync('cadena-original.txt', cadenaOriginal, 'utf-8');

  // 3. Leer llave privada
  const keyPem = fs.readFileSync(KEY_PATH, 'utf-8');
  
  // 4. Firmar
  const sign = crypto.createSign('RSA-SHA256');
  sign.update(cadenaOriginal, 'utf8');
  sign.end();
  const sello = sign.sign(keyPem, 'base64');
  console.log('\n✅ Sello generado:', sello.substring(0, 50) + '...');

  // 5. Leer certificado
  const certDer = fs.readFileSync(CER_PATH);
  const certBase64 = certDer.toString('base64');

  // 6. Insertar en XML
  let xmlSellado = xml.replace('Sello=""', `Sello="${sello}"`);
  xmlSellado = xmlSellado.replace('Certificado=""', `Certificado="${certBase64}"`);

  // 7. Guardar XML sellado
  fs.writeFileSync('cfdi-sellado.xml', xmlSellado, 'utf-8');
  console.log('💾 Guardado: cfdi-sellado.xml');

  // 8. Convertir a base64
  const xmlBase64 = Buffer.from(xmlSellado, 'utf-8').toString('base64').replace(/\r?\n|\r/g, '');

  // 9. Enviar al PAC
  console.log('\n📡 Enviando al PAC...');
  const soapRequest = `<?xml version="1.0" encoding="UTF-8"?>
<soapenv:Envelope xmlns:soapenv="http://schemas.xmlsoap.org/soap/envelope/" xmlns:ws="${SOAP_NAMESPACE}">
  <soapenv:Header/>
  <soapenv:Body>
    <ws:timbrar>
      <ws:usuario>${USER}</ws:usuario>
      <ws:password>${PASSWORD}</ws:password>
      <ws:cfdiBase64>${xmlBase64}</ws:cfdiBase64>
    </ws:timbrar>
  </soapenv:Body>
</soapenv:Envelope>`;

  fs.writeFileSync('soap-request.xml', soapRequest, 'utf-8');

  try {
    const response = await fetch('https://testing.solucionfactible.com/ws/services/Timbrado', {
      method: 'POST',
      headers: {
        'Content-Type': 'text/xml; charset=utf-8',
        'SOAPAction': ''
      },
      body: soapRequest
    });

    const responseText = await response.text();
    fs.writeFileSync('pac-response.xml', responseText, 'utf-8');
    console.log('\n📥 Respuesta del PAC guardada: pac-response.xml');

    // Parsear respuesta
    const uuidMatch = responseText.match(/<uuid>([^<]+)<\/uuid>/i);
    if (uuidMatch) {
      console.log('\n🎉 ¡TIMBRADO EXITOSO!');
      console.log('UUID:', uuidMatch[1]);
    } else {
      const faultMatch = responseText.match(/<faultstring>([^<]+)<\/faultstring>/i);
      console.log('\n❌ Error:', faultMatch ? faultMatch[1] : 'Desconocido');
    }
  } catch (error) {
    console.error('\n❌ Error de conexión:', error);
  }
}

sellarYTimbrar();
